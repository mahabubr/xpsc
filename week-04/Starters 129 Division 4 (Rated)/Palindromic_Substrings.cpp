bool isPalindrome(const string& str) {
    int left = 0;
    int right = str.length() - 1;
    while (left < right) {
        if (str[left] != str[right])
            return false;
        left++;
        right--;
    }
    return true;
}

void infinite() {
    int n;
    cin >> n;

    string s;
    cin >> s;

    if (n <= 1) {
        cout << "Bob" << endl;
        return;
    }

    int count = 0;
    for (int i = 0; i < n; ++i) {
        for (int j = i; j < n; ++j) {
            string sub = s.substr(i, j - i + 1);
            if (isPalindrome(sub))
                count++;
        }
    }

    if (count % 2 == 0) {
        cout << "Bob" << endl;
    }
    else {
        cout << "Alice" << endl;
    }
}

