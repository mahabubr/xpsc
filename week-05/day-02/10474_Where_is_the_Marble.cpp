void infinite() {
    // write code
    int n, q;
    cin >> n >> q;

    vi arr(n);

    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }

    sort(arr.begin(), arr.end());


    while (q--) {
        int x;
        cin >> x;

        auto it = lower_bound(arr.begin(), arr.end(), x);

        if (it != arr.end() && *it == x) {
            cout << x << " found at " << it - arr.begin() + 1 << endl;
        }
        else {
            cout << x << " not found" << endl;
        }
    }

}

