char convertToChar(int n) {
    return (char)(n + 'a' - 1);
}

void infinite() {
    // write code
    int n;
    cin >> n;

    string s;
    cin >> s;

    string str;

    for (int i = n - 1; i >= 0; i--) {
        if (s[i] == '0') {
            string concatStr = string(1, s[i - 1]) + string(1, s[i - 2]);
            reverse(concatStr.begin(), concatStr.end());

            str += convertToChar(stoi(concatStr));

            i -= 2;
        }
        else {
            str += convertToChar((int)(s[i] - '0'));
        }
    }

    reverse(str.begin(), str.end());

    cout << str << endl;
}

