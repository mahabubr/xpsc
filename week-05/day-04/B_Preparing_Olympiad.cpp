void infinite() {
    // write code
    int n, l, r, x;
    cin >> n >> l >> r >> x;

    vi arr(n);

    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }

    int ans = 0;

    for (int i = 0;i < (1 << n); i++) {
        int sum = 0, mn = INT_MAX, mx = INT_MIN;
        for (int j = 0; j < n; j++) {
            if (((i >> j) & 1) == 1) {
                sum += arr[j];
                mx = max(mx, arr[j]);
                mn = min(mn, arr[j]);
            }
        }

        if (sum >= l && sum <= r && (mx - mn) >= x) {
            ans++;
        }

    }

    cout << ans << endl;
}

