string LCS(string& str1, string& str2) {
    if (str1.empty() || str2.empty()) {
        return "";
    }

    int n = str1.size();
    int m = str2.size();

    vector<vi> dp(n + 1, vi(m + 1, 0));
    int max_len = 0, end_index = 0;

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (str1[i - 1] == str2[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1] + 1;
                if (dp[i][j] > max_len) {
                    max_len = dp[i][j];
                    end_index = i - 1;
                }
            }
        }
    }

    return str1.substr(end_index - max_len + 1, max_len);
}


