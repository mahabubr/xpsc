    ll n;
    cin >> n;

    vi arr(n);

    for (ll i = 0; i < n; i++) {
        cin >> arr[i];
    }

    ll neg = 0;

    for (ll i = 0; i < n; i++) {
        if (arr[i] < 0) {
            neg++;
            arr[i] = abs(arr[i]);
        }
    }

    ll sum = 0, mn = m_inf;

    for (ll i = 0; i < n; i++) {
        sum += arr[i];
        mn = min(mn, arr[i]);
    }

    if (neg % 2 == 1) {
        sum -= 2 * mn;
    }

    cout << sum << endl;
