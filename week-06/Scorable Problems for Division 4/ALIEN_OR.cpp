int binaryToDecimal(string n) {
    int dec_value = 0;
    int base = 1;

    for (int i = n.length() - 1; i >= 0; i--) {
        if (n[i] == '1')
            dec_value += base;
        base = base * 2;
    }

    return dec_value;
}

